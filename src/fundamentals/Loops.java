package fundamentals;

public class Loops {

	public static void main(String[] args) {
		
		
		for(int i = 1; i<=10;i++) {
			System.out.println("THis is For Loop");			
		}
		
		int i = 1;
		
		while(i<=10) {
			System.out.println("This is While Loop");
			i++;
		}
		
		i = 1;
		
		do {
			System.out.println("This is Do While Loop");
			i++;
		}while(i<=10);
		
		for(int j = 1; j<=10; j++) {
			System.out.println(j);
		}
	}

}
